import React from 'react'

function Tbody({getTableBodyProps,prepareRow,rows}) {
  return (
      <tbody style={{textAlign:'center'}} {...getTableBodyProps()}>
        {
            // Loop over the table rows
            rows.map((row) => {
              // Prepare the row for display
              prepareRow(row);
              return (
                // Apply the row props
                <tr {...row.getRowProps()}>
                  {
                    // Loop over the rows cells
                    row.cells.map((cell) => {
                      // Apply the cell props
                      return (
                        <td style={{padding:'2vh '}} {...cell.getCellProps()}>
                          {
                            // Render the cell contents
                            cell.render("Cell")
                          }
                        </td>
                      );
                    })
                  }
                </tr>
              );
            })
          }
      </tbody>
  )
}

export default Tbody